			<center><font size="6"><b>Introdução</b></font></center>
			<p>
				As fontes de alimentação servem para converter a <b>tensão alternada</b> de uma rede elétrica, em <b>tensões contínuas</b>. As conversões, são importantes para que os computadores não sejam danificados, pois está ligada diretamente a <b>estabilidade, possibilidade de expansão e a sua durabilidade do aparelho</b>.
			</p>
			<center><img src="imagens/C2/foto1.jpg" width="25%"><br><br>

			<font size="6"><b>Introdução Elétrica</b></font></center>
			<p>
				A <b>tensão elétrica</b> é uma diferença entre o <b>potencial elétrico</b> de <b>dois pontos</b>, ou seja, é a <b>força necessária para o movimento dos elétrons</b> para ter uma <b>corrente elétrica</b>.
			</p>
			<ul>
				<li>
					Diferença de potencial, pode representar uma <b>fonte de energia</b>.<br>
					<small>(A fonte de energia da nossa residência são as usinas Termoelétricas, Hidroelétricas e Eólicas – Geradores).</small>
				</li>
				<li>
					<b>Geradores de tensão</b>, é um mecanismo que aparece entre dois terminais denominados <b>polos</b>, uma <b>diferença de potencial ou tensão elétrica</b> que aparece porque <b>um dos polos terá excesso de elétrons</b> (polo negativo), e o outro <b>faltará elétrons</b> (polo positivo).
				</li>
			</ul><br><br>

			<center><font size="5"><b>A palavra <b style="color: red">“Voltagem”</b> existe?</b></font></center>
			<p>
				A palavra <b>voltagem</b> não existe, a sua denominação correta é <b>tensão elétrica</b> ou <b>diferença de potencial elétrico</b> (DDP). O que acontece, é que pela utilização do senso comum a palavra <b>voltagem</b> induz a acreditar que é a correta, somente pela <b>unidade</b> que representa a <b>tensão elétrica</b> que é <b>VOLT</b>.
			</p><br>

			<center><font size="5"><b>Tensão Alternada (ACV)</b></font></center>
			<p>
				Fonte de energia elétrica produzidas por <b>geradores</b>, como a <b>energia eletromecânica</b>. São tensões de polaridades alternadas, alternando <b>negativo e positivo</b> em decorrência do <b>tempo em ciclos</b>, ou seja, <b>movimentos dos elétrons em duas direções</b>.<br><br>
				A quantidade dos ciclos em relação ao tempo que é denominado <b>frequência</b> e sua medida é de ciclos por segundos <b><i>“Hertz”</i></b>.
			</p>
			<center><img src="imagens/C2/foto2.jpg" width="30%"></center><br>
			<p>
				<b style="font-size: 19px;">Curiosidade</b><br><br>
				No Brasil, é determinado que a frequência é de <b>60 HZ</b>, ou seja, as lâmpadas estão piscando 60 vezes por segundo, mas isso é tão rápido que não é possível de se perceber.
			</p><br>

			<center><font size="5"><b>Valores de tensão</b></font></center>
			<p>
				Internacionalmente, existem <b>dois valores</b> para a tensão elétrica da rede elétrica domiciliar, que são <b style="color: red">115V e 230V</b>. No Brasil, utilizamos <b style="color: red">110V e 220V</b>, embora as redes <b style="color: red">110V</b> trabalhem à <b style="color: red">127V</b>.
			</p><br>

			<center><font size="5"><b>Tensão Contínua (DCV)</b></font></center>
			<p>
				A tensão contínua, é a que <b>movimenta os elétrons sempre para a mesma direção</b>. Essa tensão, é utilizada em <b>pilhas e baterias</b>, ou nos circuitos internos de aparelhos elétricos, pois trabalha com valores baixos como, <b style="color: red">3.6V, 5V, 12V, 24V</b>.
			</p>
			<center><img src="imagens/C2/foto3.jpg" width="30%"><br><br>

			<font size="5"><b>Corrente Elétrica</b></font></center>
			<p>
				<b>Movimento ordenado das cargas elétricas</b> (partículas eletrizadas chamadas de íons ou elétrons) dentro de um <b>sistema condutor</b> e sua unidade de medida é <b>AMPERE (A)</b>.<br>Existem dois tipos:
			</p>
			<ul>
				<li>Corrente contínua</li>
				<li>Corrente alternada</li>
			</ul>
			<center><img src="imagens/C2/foto4.jpg" width="20%"><br><br>

			<font size="5">Corrente Contínua</font></center>
			<p>
				Uma corrente é contínua, quando <b>não altera o seu sentido</b>, sempre positiva ou negativa. Esta, é constante e é comumente encontrado em pilhas e baterias.
			</p>
			<center><img src="imagens/C2/foto5.jpg" width="35%"><br><br>

			<font size="5">Corrente Alternada</font></center>
			<p>
				Corrente que é invertida periodicamente, ou seja, ora é <b>positiva</b> e ora é <b>negativa</b>, fazendo com que os elétrons executem um movimento de <b>vai-e-vem</b>.
			</p>
			<center><img src="imagens/C2/foto6.jpg" width="40%"><br><br>

			<font size="5"><b>Potência Elétrica</b></font></center>
			<p>
				Definida pela rapidez com que um trabalho é realizado, ou seja, é a <b>medida do trabalho realizado</b> em relação a uma <b>unidade de tempo</b>. Nos equipamentos elétricos, a potência indica a quantidade de energia elétrica que foi transformada em outro tipo de energia, por unidade de tempo. A unidade de potência é <b>WATT (W)</b>.<br><br>
				<center><b style="font-size: 30px;">P = U.i</b></center><br>
			</p>
			<p>
				<b>Exemplo:</b><br>
				Ao adquirir uma fonte de alimentação para dispositivos móveis, como notebook, celulares, entre outros, precisa-se observar a tensão e a corrente com que trabalha.
			</p>
			<center>
				<img src="imagens/C2/foto7.jpg" width="30%"><br><br><br>

				<table id="sintaxe" width="10%">
					<tr><td style="text-align: center; font-size: 18px;"><b>Atenção</b></td></tr>
				</table>
			</center>
			<p>
				Os técnicos muitas vezes não se preocupam com a segurança dos equipamentos, por isso, é muito importante não <b>ignorarem</b> a questão do <b>aterramento dos aparelhos</b>;. O que acontece, é que geralmente o <b>pino de aterramento é removido</b> e com isso o aparelho está em risco, como também todos há quem está conectado.
			</p>
			<center><img src="imagens/C2/foto8.jpg" width="15%"></center><br><br>

			<font size="4" style="margin-left: 7%;">→ Tomadas e suas corretas instalações:</font><br>
			<center><img src="imagens/C2/foto9.jpg" width="40%"></center>
			<p>
				O <b>perigo</b> mais frequente, é o de se <b>conectar a energia em uma fonte de alimentação</b> ou até mesmo um <b>estabilizador</b> em uma tomada que está com a sua <b>polaridade incorreta e/ou sem aterramento</b>.<br><br>
				Toda parte <b>metálica</b> do gabinete recebe uma tensão de <b>220V</b> no caso da segunda imagem com seus polos invertidos na tomada. Quando conectado inadequadamente, o problema não afeta somente o equipamento, mas também tem grandes chances pode pôr em <b>risco o usuário</b> de <b>sofrer um choque elétrico</b>, que pode ser até <b>fatal</b>.
			</p><br>

			<font size="4" style="margin-left: 7%;">→ E por último:</font><br>
			<p>
				Deve-se verificar se a fonte de alimentação possui uma <b>chave seletora</b> da <b>tensão de entrada</b> por nem todas as fontes vem do tipo <b><i>AUTO SWITCH</i></b>. Esses tipos de fontes, são aquelas que ajustam automaticamente a tensão de entrada ao serem ligadas à energia.
			</p>
