			<center><font size="6"><b>Introdução</b></font></center><br>
			<center><img src="imagens/C1/foto1.jpg" width="32%"></center>
			<p>
				A <b>informática</b>, é termo usado para descrever o conjunto das ciências relacionadas a armazenamento, transmissão e processamentos da informação em meios digitais.<br><br><br>

				<b style="font-size: 19px;">O que é um computador?</b><br><br>
				O computador é o elemento físico utilizado para obtenção da informação. É uma máquina construída por uma série de componentes e <b>circuitos eletrônicos</b>, a qual é capaz de receber, armazenar, processar e transmitir as <b>informações</b>.<br><br>
				Esse equipamento, é programável, capaz de realizar tarefas seguindo uma sequência de comandos. O computador não nada, sem que seja ordenado a fazer.
			</p>

