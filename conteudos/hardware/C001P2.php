					<font size="6"><b>Conceitos de Hardware e Software</b></font><br></center>
					<p>
						O <b>sistema computacional</b> é um conjunto de dispositivos eletrônicos (<i>hardware</i>), capazes de processar dados de acordo com um programa (<i>software</i>).<br><br>
						O principal <i>software</i>, é o <b>sistema operacional (SO)</b>, pois é ele quem “dá vida” ao <i>hardware</i> e fornece as bases para a execução de programas.
					</p><br>

				<center><font size="6"><b>Hardware</b></font><br></center>
				<p>
					O <i><b>hardware</b></i> de um computador pode ser programado para realizar diversas tarefas, as quais precisam de <b>entradas</b> para fornecerem <b>saída</b>.<br><br>
					Então, os <b>dados</b> (entrada) <b>processador</b> é o responsável por processar os dados e fornecer a <b>informação</b> desejada (dados processados).
				</p>
				<center><img src="imagens/C1/foto1.1.jpg" width="45%"><br><br><br><br>
				<img src="imagens/C1/foto2.1.jpg" width="50%"><br><br><br>

				<font size="6"><b>Componentes de hardware</b></font><br></center>
				<p>
					<b style="font-size: 19px;">Fonte de alimentação:</b> Dispositivo responsável por <b>fornecer energia elétrica aos componentes de um computador</b>. Ademais, é um tipo de equipamento que deve ser escolhido e manipulado com cuidado, pois, qualquer equívoco pode resultar em provimento inadequado de eletricidade ou em danos à máquina.
				</p>
				<center><img src="imagens/C1/foto3.1.jpg" width="20%"></center>

				<p>
					<b style="font-size: 19px;">Placa mãe (<i>Motherboard</i>):</b> A placa-mãe é uma das principais peças do computador e todos os seus periféricos são acoplados: placa de vídeo, rede, som, memória, HD, processador. Para que estas peças sejam acopladas, é necessária a utilização de slots disponíveis, tais como: PCI e PCI <i>Express</i>.
				</p>
				<center><img src="imagens/C1/foto4.1.jpg" width="40%"></center>

				<p>
					<b style="font-size: 19px;">Processador:</b> O microprocessador (UCP – unidade central de processamento) é uma das peças mais importantes de um computador. Ele é o “cérebro” da máquina, é um circuito integrado (com milhões de transístores) que realiza a tomada de decisões e os cálculos.<br>
					Principais fabricantes são: <b>Intel e AMD</b>.
				</p>
				<center><img src="imagens/C1/foto5.1.jpg" width="40%"></center>

				<p>
					<b style="font-size: 19px;">Memória:</b> Principais tipos de memória:<br><br>
					&nbsp;&nbsp;<b>RAM:</b> (<i>Random Acess Memory</i>).<br>
					&nbsp;&nbsp;<b>ROM:</b> (<i>Read Only Memory</i>).<br><br>
					Unidades de capacidade de memória: <b><i>MegaByte, GigaByte</i></b>.<br><br>

					<b>Memória RAM:</b> A memória RAM, é um <i>hardware</i> de armazenamento <b>randômico e volátil</b> de memória. Isto é, a peça armazena dados de programas em execução, enquanto o computador está ligado.
				</p>

				<ul>
					<li>Memória volátil onde são carregados programas e dados, que são lidos do disco rígido (HD).</li>
					<li>Permite leitura e gravação.</li>
					<li>Muito mais veloz que as memórias secundárias (HD, DVD-ROM).</li>
				</ul><br>
			<center>
				<img src="imagens/C1/foto6.1.jpg" width="26%">
				<p>
					<b>Memória ROM:</b> Tipo de memória que permite apenas a <b>leitura</b>, ou seja, suas informações são gravadas pelo fabricante uma única vez e após isso não podem serem alteradas ou apagadas, somente acessadas.
				</p>
			</center>
				<ul>
					<li>Memória <b>não volátil</b> onde são armazenadas as rotinas de inicialização do computador. Ex: BIOS.</li>
					<li>Permite apenas a leitura e é gravada pelo fabricante.</li>
				</ul><br>

